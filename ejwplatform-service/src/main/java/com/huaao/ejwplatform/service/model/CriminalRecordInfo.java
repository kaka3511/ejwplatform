package com.huaao.ejwplatform.service.model;

import java.util.Date;

public class CriminalRecordInfo extends AbstractJsonModel{

	private static final long serialVersionUID = 18192831930123L;
	
	private String id;
    private String userId;
    private String name;
    private Integer status;
    private Integer authStatus;
    private String authDesc;
    private Date updateTime;
    private Integer recieveType;
    private String qrcode;
    /**
     * 受理单位名称
     */
    private String deptName;
    /**
     * 失效时间
     */
    private Date invalidTime;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Integer getAuthStatus() {
		return authStatus;
	}
	public void setAuthStatus(Integer authStatus) {
		this.authStatus = authStatus;
	}
	public String getAuthDesc() {
		return authDesc;
	}
	public void setAuthDesc(String authDesc) {
		this.authDesc = authDesc;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	public Integer getRecieveType() {
		return recieveType;
	}
	public void setRecieveType(Integer recieveType) {
		this.recieveType = recieveType;
	}
	public String getQrcode() {
		return qrcode;
	}
	public void setQrcode(String qrcode) {
		this.qrcode = qrcode;
	}
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public Date getInvalidTime() {
		return invalidTime;
	}
	public void setInvalidTime(Date invalidTime) {
		this.invalidTime = invalidTime;
	}
}
