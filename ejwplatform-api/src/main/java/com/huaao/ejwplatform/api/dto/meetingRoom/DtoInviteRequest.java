package com.huaao.ejwplatform.api.dto.meetingRoom;

import org.springframework.web.bind.annotation.RequestParam;

import com.huaao.ejwplatform.api.dto.pub.DtoPublicRequest;

public class DtoInviteRequest extends DtoPublicRequest{
	
	private String inviter ;
	private String topic;
	private Long time;
	private String roomid;
	private String roomidstr;
	private String roomname;
	private String password;
	private String uidStr;
	private String message;
	public String getInviter() {
		return inviter;
	}
	public void setInviter(String inviter) {
		this.inviter = inviter;
	}
	public String getTopic() {
		return topic;
	}
	public void setTopic(String topic) {
		this.topic = topic;
	}
	public Long getTime() {
		return time;
	}
	public void setTime(Long time) {
		this.time = time;
	}
	public String getRoomid() {
		return roomid;
	}
	public void setRoomid(String roomid) {
		this.roomid = roomid;
	}
	public String getRoomidstr() {
		return roomidstr;
	}
	public void setRoomidstr(String roomidstr) {
		this.roomidstr = roomidstr;
	}
	public String getRoomname() {
		return roomname;
	}
	public void setRoomname(String roomname) {
		this.roomname = roomname;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getUidStr() {
		return uidStr;
	}
	public void setUidStr(String uidStr) {
		this.uidStr = uidStr;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	
	
	
}
