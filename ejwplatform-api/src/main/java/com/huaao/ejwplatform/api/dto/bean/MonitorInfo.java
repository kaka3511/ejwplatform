package com.huaao.ejwplatform.api.dto.bean;

public class MonitorInfo {

	private String id;

	private String monitorDeviceId;

	private String name;

	private Integer channelnumber;

	private String location;

	private String description;

	private String groupid;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getMonitorDeviceId() {
		return monitorDeviceId;
	}

	public void setMonitorDeviceId(String monitorDeviceId) {
		this.monitorDeviceId = monitorDeviceId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getChannelnumber() {
		return channelnumber;
	}

	public void setChannelnumber(Integer channelnumber) {
		this.channelnumber = channelnumber;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getGroupid() {
		return groupid;
	}

	public void setGroupid(String groupid) {
		this.groupid = groupid;
	}

	public void setDeviceid(String deviceid) {
		this.monitorDeviceId = deviceid ;
	}
}
