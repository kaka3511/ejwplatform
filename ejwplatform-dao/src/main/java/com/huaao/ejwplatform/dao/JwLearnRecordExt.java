package com.huaao.ejwplatform.dao;

import java.io.Serializable;
import java.util.Date;

public class JwLearnRecordExt implements Serializable{

	private static final long serialVersionUID = 3177983465617410288L;

	private String name;
	
    private Date time;
    
    private String uid;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Date getTime() {
		return time;
	}

	public void setTime(Date time) {
		this.time = time;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}
}
